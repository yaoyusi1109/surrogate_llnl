function y = borehole(x)

  rw = x(:,1).*(0.15-0.05)+0.05;
  r  = x(:,2).*(50000-100)+100;
  Tu = x(:,3).*(115600-63070)+63070;
  Hu = x(:,4).*(1110-990)+990;
  Tl = x(:,5).*(116-63.1)+63.1;
  Hl = x(:,6).*(820-700)+700;
  L  = x(:,7).*(1680-1120)+1120;
  Kw = x(:,8).*(15000-1500)+1500;

  frac1 = 2*pi.*Tu.*(Hu-Hl);
  frac2a = 2.*L.*Tu./(log(r./rw).*rw.^2.*Kw);
  frac2b = Tu./Tl;
  frac2 = log(r./rw).*(1+frac2a+frac2b);
  y = frac1./frac2;
  y = (y-59)./49;

end
